export function sourceCoverageSummary(sources: OfficialSourceGuide[]) {
  const levels = new Map<string, number>()
  for (const source of sources) levels.set(source.evidenceLevel, (levels.get(source.evidenceLevel) ?? 0) + 1)
  return Array.from(levels.entries())
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([level, count]) => `${level}: ${count}`)
    .join(' / ')
}

export function autoFetchSummary(sources: OfficialSourceGuide[]) {
  const counts = new Map<string, number>()
  for (const source of sources) counts.set(source.autoFetch, (counts.get(source.autoFetch) ?? 0) + 1)
  return ['enabled', 'partial', 'manual', 'restricted']
    .map((key) => `${key}: ${counts.get(key) ?? 0}`)
    .join(' / ')
}
